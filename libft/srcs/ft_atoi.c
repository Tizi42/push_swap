int	ft_atoi(const char *str)
{
	int n;
	int signe;

	n = 0;
	signe = 1;
	while (*str == ' ' || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			signe = -1;
		str++;
	}
	while (*str > 47 && *str < 58)
	{
		n *= 10;
		n += (*str - 48);
		str++;
	}
	n *= signe;
	return (n);
}
